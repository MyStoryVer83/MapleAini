function start() {
    status = -1;
    action(1, 0, 0);
}

function action(mode, type, selection) {
    if (mode == -1) {
	cm.sendNext("好好享受你的冒险之旅吧。");
        cm.dispose();
    } else {
        if (status == 0 && mode == 0) {
		cm.sendNext("好好享受你的冒险之旅吧。");
		cm.dispose();
        }
        if (mode == 1)
            status++;
        else
            status--;
        if (status == 0) 
			cm.sendYesNo("你想跳过彩虹岛教程直接去明珠港？");
	else if (status == 1){
			cm.warp(104000000);
			cm.dispose();
		}
	}
}